<h1 style="text-align:center;color:Crimson">History</h1>
<p style= "font-size:30px;">&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;In April 2009, Loyola College, Chennai, founded in 1925, and the Jesuit<b>Institut Catholique d’Arts et Métiers</b>,founded in 1898 in Lille, France, entered on a joint venture to found an engineering college in Chennai which would prepare Indian students to work in European industries and European students to work in Indian industries. Pursuant of this agreement, the Jesuits of Madurai Province founded Loyola-ICAM engineering college at the Loyola College campus in 2010.<b>It was officially inaugurated by Kalaignar M. Karunanidhi on 27 October 2010</b>.Based on principles of the Jesuit Ratio Studiorum with its humanistic emphasis, the study program aims at comprehensive personal development and at the ability to work as a team, develop communication skills, appreciate cultures, and be sensitive to ethical and social issues.</p>

<h1 style="text-align:center;color:Crimson">Academics</h1>
<p style= "font-size:30px;">
&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;The college <b>offers courses at undergraduate levels (Bachelors) in engineering and technology</b>, including Mechanical Engineering, Electrical and Electronics Engineering, Electronics and Communication Engineering, Computer Science Engineering and Information Technology.In 2015–16, the sanctioned intake was 60/course, but 120 for Mechanical Engineering.<b>One aspect of Loyola College's affiliation with the Catholic Institute of Arts & Business (ICAM) in Lille, France, is the Master of Engineering degree for which Loyola-ICAM students may study for two years in France, after finishing their Bachelor studies in India. Students at Lille, in turn, can spend their fifth year of studies for the Master of Engineering in India</b>.</p>

<h1 style="text-align:center;color:Crimson">Facilities</h1>
<p style= "font-size:30px;">
&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;LICET library contains over 25,000 books and receives 42 technical journals, besides E-resources like IEEE, ASME, ASTM, Springer, and Elsevier. It has Wi-Fi facility and has a capacity to accommodate 445 students.<br><br>

&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;Mechatronics lab to train students in such areas as <b>hydraulics and pneumatics, microcontrollers and PID controllers, as well as simulating pressure, flow, and temperature</b>for process and automation industries. The lab has simulators, a robot and a <b> human machine interface (HMI)</b> to control, monitor and integrate factory automation processes.<br><br>

&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;<b>Centre for Intelligent Systems (CIS)</b> facilitates students do web and mobile-based projects. Workshops related to Android and iOS application development are conducted by experts from the industry. Currently CIS is used as the centre for developing an Android/iOS Mobile App for an international client in France.<br><br>

